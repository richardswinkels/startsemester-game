class Button
{
private:
  uint pin;
  ulong lastPressTime = 0;
  const ulong debounceDelay = 50;

public:
  Button(int btnPin)
  {
    pin = btnPin;
  }

  void init()
  {
    pinMode(pin, INPUT_PULLUP);
  }

  bool isPressed()
  {
    ulong currentMillis = millis();

    if (currentMillis - lastPressTime > debounceDelay &&
        digitalRead(pin) == LOW)
    {
      lastPressTime = currentMillis;
      return true;
    }

    return false;
  }
};

