class ApiClient
{
private:
  HTTPClient http;
  WiFiClient wifiClient;
  const String apiUrl = "http://spaceinvaders.requestcatcher.com/test";

public:
  void postScore(String playerName, int score, int bulletsShot)
  {
    if (WiFi.status() == WL_CONNECTED)
    {
      JsonDocument jsonDoc;
      jsonDoc["player_name"] = playerName;
      jsonDoc["score"] = score;
      jsonDoc["bullets_shot"] = bulletsShot;

      String jsonData;
      serializeJson(jsonDoc, jsonData);

      http.begin(wifiClient, apiUrl);
      http.addHeader("Content-Type", "application/json");
      http.POST(jsonData);
      http.end();
    }
  };
};

class Game
{
private:
  ApiClient apiClient;
  Player player;
  Drone drones[4][8];
  Bullet playerBullet;
  Bullet enemyBullets[4];
  ulong timeEllapsed;
  bool isRunning;

  void initDrones()
  {
    for (int row = 0; row < 4; row++)
    {
      for (int col = 0; col < 8; col++)
      {
        drones[row][col].setPosition(6 + col * (11 + 4), 30 + row * (11 + 4));
        drones[row][col].enable();
      }
    }
  }

  void drawDrones()
  {
    for (int row = 0; row < 4; row++)
    {
      for (int col = 0; col < 8; col++)
      {
        if (drones[row][col].getActiveState())
          drones[row][col].draw();
      }
    }
  }

  void drawLives()
  {
    for (int col = 0; col < player.getLives(); col++)
    {
      canvas.drawRGBBitmap(115 - (10 + 4) * col, 5, heartSprite, 10, 9);
    }
  }

  void drawEnemyBullets()
  {
    for (int i = 0; i < 3; i++)
    {
      enemyBullets[i].draw();
    }
  }

  void updatePlayerBullet()
  {
    if (playerBullet.getActiveState())
      playerBullet.move(0, 1);
    if (playerBullet.getPosY() < 0)
      playerBullet.disable();
  }

  void updateEnemyBullets()
  {
    for (int i = 0; i < 3; i++)
    {
      Bullet &bullet = enemyBullets[i];

      if (bullet.getActiveState())
        bullet.move(0, -1);
      if (bullet.getPosY() > 160)
        bullet.disable();
    }
  }

  Drone *getRandomActiveDrone()
  {
    uint activeDroneIndexes[32][2];
    uint activeDroneCount = 0;

    for (int row = 0; row < 4; row++)
    {
      for (int col = 0; col < 8; col++)
      {
        if (drones[row][col].getActiveState())
        {
          activeDroneIndexes[activeDroneCount][0] = row;
          activeDroneIndexes[activeDroneCount][1] = col;
          activeDroneCount++;
        }
      }
    }

    if (activeDroneCount == 0)
    {
      return nullptr;
    }

    uint randomIndex = random(0, activeDroneCount);
    uint randomRow = activeDroneIndexes[randomIndex][0];
    uint randomCol = activeDroneIndexes[randomIndex][1];

    return &drones[randomRow][randomCol];
  }

  void checkDroneBulletCollisions()
  {
    for (int row = 0; row < 4; row++)
    {
      for (int col = 0; col < 8; col++)
      {
        Drone &drone = drones[row][col];

        if (drone.getActiveState() &&
            playerBullet.getActiveState() &&
            drone.collidesWith(playerBullet))
        {
          drone.disable();
          playerBullet.disable();
          player.setScore(player.getScore() + 10);
        }
      }
    }
  }

  void checkPlayerBulletCollisions()
  {
    for (int i = 0; i < 3; i++)
    {
      Bullet &bullet = enemyBullets[i];

      if (bullet.getActiveState() &&
          bullet.collidesWith(player))
      {
        bullet.disable();
        player.setLives(player.getLives() - 1);
      }
    }
  }

  void shootEnemyBullets()
  {
    ulong currentMillis = millis();

    if (currentMillis - timeEllapsed >= 1000)
    {
      timeEllapsed = currentMillis;

      for (int i = 0; i < random(1, 4); i++)
      {
        Drone *drone = getRandomActiveDrone();

        if (drone)
        {
          for (int j = 0; j < 3; j++)
          {
            Bullet &bullet = enemyBullets[j];

            if (!bullet.getActiveState())
            {
              drone->shoot(enemyBullets[j]);
              break;
            }
          }
        }
      }
    }
  }

  bool allDronesDisabled()
  {
    int count = 0;
    for (int row = 0; row < 4; row++)
    {
      for (int col = 0; col < 8; col++)
      {
        if (!drones[row][col].getActiveState())
          count++;
      }
    }
    return count == 32;
  }

  bool isGameOver()
  {
    return player.getLives() == 0;
  }

public:
  void init()
  {
    isRunning = true;
    player.setBulletsShot(0);
    player.setScore(0);
    player.setPosition(58, 140);
    player.setLives(3);
    playerBullet.disable();
    initDrones();
  }

  void handleInput()
  {
    if (btnLeft.isPressed())
    {
      player.move(-1);
    }

    if (btnRight.isPressed())
    {
      player.move(1);
    }

    if (btnShoot.isPressed())
    {
      player.shoot(playerBullet);
    }
  }

  void update()
  {
    updatePlayerBullet();
    updateEnemyBullets();

    if (allDronesDisabled())
    {
      playerBullet.disable();
      initDrones();
      delay(500);
    }

    shootEnemyBullets();

    if (isGameOver())
    {
      apiClient.postScore(player.getName(), player.getScore(), player.getBulletsShot());
      isRunning = false;
      currentGameScene = GameSceneList::GameOverScene;
    }
  }

  void checkCollisions()
  {
    checkDroneBulletCollisions();
    checkPlayerBulletCollisions();
  }

  void render()
  {
    canvas.fillScreen(ST7735_BLACK);
    player.draw();
    playerBullet.draw();
    drawDrones();
    drawLives();
    drawEnemyBullets();

    canvas.setCursor(5, 5);
    canvas.setTextColor(ST7735_WHITE);
    canvas.setTextSize(1);
    canvas.print(player.getScore());

    tft.drawRGBBitmap(0, 0, canvas.getBuffer(), 128, 160);
  }

  Player *getPlayer()
  {
    return &player;
  }

  bool getRunningState()
  {
    return isRunning;
  }
};

